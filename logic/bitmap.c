void OLED_Convert_CF1_To_Buffer(
    const uint8_t *src,
    uint8_t *dst,
    uint16_t width,
    uint16_t height
){
    for (uint16_t i = 0; i < width * (height / 8); i++){
        dst[i] = 0x00;
    }

    for (uint16_t y = 0; y < height; y++){
        for (uint16_t x = 0; x < width; x++){

            uint32_t bit_index  = y * width + x;
            uint32_t byte_index = bit_index >> 3;
            uint8_t  bit_pos    = 7 - (bit_index & 7);

            if (src[byte_index] & (1 << bit_pos)){
                uint32_t page = y >> 3;
                uint32_t dst_index = page * width + (width - 1 - x);
                dst[dst_index] |= (1 << (y & 7));
            }
        }
    }
}
